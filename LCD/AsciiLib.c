void SetFont(uint8_t num)
{
	if (num < 2)
	{
		_selectFont = num;
	}
}

uint8_t GetFont(void)
{
	return _selectFont;
}

uint16_t GetWidth(uint8_t* text)
{
	uint16_t res = 0;
	while(*text)
	{
		uint8_t c = *text++;
		res += GetCharWidth(c);
	}
	return res;
}

uint8_t GetCharWidth(uint8_t c)
{
	c -= 32;
	if (c - 64 >= 96) // RUS WIN-1251
	{
		c -= 64;
	}
	switch(_selectFont)
	{
		case 0:
			return 5+1;
		case 1:
			return Font8x15_Width[c]+1;
		default:
			return 5+1;
	}
}

uint8_t GetCharHeight(void)
{
	switch(_selectFont)
	{
		case 0:
			return 8;
		case 1:
			return 15;
		default:
			return 8;
	}
}

void GetCharSize(uint8_t* w, uint8_t* h, uint8_t c)
{
	c -= 32;
	if (c - 64 >= 96) // RUS WIN-1251
	{
		c -= 64;
	}
	switch(_selectFont)
	{
		case 0:
			*w = 5; *h = 8;
			break;
		case 1:
			*w = Font8x15_Width[c]; *h = 15;
			break;
		default:
			*w = 5; *h = 8;
			break;
	}
}

unsigned char* GetASCIICode(char ASCII)
{  
	ASCII -= 32;
	if (ASCII - 64 >= 96) // RUS WIN-1251
	{
		ASCII -= 64;
	}

	switch(_selectFont)
	{
		case 0:
			return (unsigned char*)Font5x8[ASCII];
		case 1:
			return (unsigned char*)Font8x15[ASCII];
		default: 
			return (unsigned char*)Font5x8['?'-32];
	}
}

