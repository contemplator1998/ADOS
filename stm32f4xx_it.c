/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
bool state = false;
int time_counter = 0;
void SysTick_Handler(void)
{
	if (time_counter++ > 1000)
	{
		if (GPIO_ReadInputDataBit(GPIOC,GPIO_Pin_5))
		{
			DrawClock(MAIN_CLOCK_X, MAIN_CLOCK_Y);
			time_counter = 0;
		}	
		else
		{
			time_counter = 800;
		}
	}
}

